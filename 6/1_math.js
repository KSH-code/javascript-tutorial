/**
 * 이 외에도 많은 기능들이 있지만, 자주 사용하는 기능들만 적어보겠다.
 */
console.log()
console.log(Math.floor(1.1)) //소수 자리는 버린다.
console.log(Math.ceil(1.1)) // 올린다.
console.log(Math.min(1, 2, 3)) // 작은 수를 출력한다.
console.log(Math.max(1, 2, 3)) //큰 수를 출력한다.
console.log(Math.random()) //0~1 사이의 난수를 출력한다.
console.log(Math.round(1.9)) //반올림
console.log(Math.sqrt(2))//제곱근