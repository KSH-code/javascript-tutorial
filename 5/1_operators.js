var x = 1
console.log(x)//1
x+=1
console.log(x)//x + 1
x-=1
console.log(x)//x - 1
x*=2
console.log(x)//x * 2
x/=2
console.log(x)//2 | x
x%=2
console.log(x)//x mod 2
x<<=1
console.log(x)// 0001 -> 0010
x>>=1
console.log(x)// 0010 -> 0001
x>>>=1
console.log(x)// 0001 -> 0000 (부호없는)
x&=1
console.log(x)// 0000 같은값이 없음 0000 = 0
x^=1
console.log(x)// 0000 0001 xor = 1
x|=1
console.log(x)// 0000 0001 or = 1
